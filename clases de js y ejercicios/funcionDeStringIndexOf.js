// index of te dan el primero de las posiciones de lo que te pida
// lastIndexOf te da el ultimo de los valores

var example = "the quick brown fox jumps over the lazy dog";
var position = example.indexOf("o"); //el valor de la primera o
var position2 = example.lastIndexOf("o");// el valor de la ultima o
console.log(position);
console.log(position2);

